#Exemplo de string
texto = "Olá, Mundo!"
#Fatiar do imício até um íncide específico
print(texto[:5])
#Fatiar a partir de um indíce específico até o final
print(texto[5:])
#Fatiar do início até o final, com um passo específico
print(texto[::2])
#Fatiar com início e fim específicos
print(texto[3:8])
#Fatiar com passo negativo para inverter uma substring
print(texto[::-1])

